public class Iv {
    double ivHp;
    double ivAtk;
    double ivSpaAtk;
    double ivDef;
    double ivSpaDef;
    double ivSpeed;

    public Iv(double ivHp, double ivAtk, double ivSpaAtk, double ivDef, double ivSpaDef,
            double ivSpeed) {
                this.ivHp = ivHp;
                this.ivAtk = ivAtk;
                this.ivSpaAtk = ivSpaAtk;
                this.ivDef = ivDef;
                this.ivSpaDef = ivSpaDef;
                this.ivSpeed = ivSpeed;
            }
}
