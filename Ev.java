public class Ev {
    double evHp;
    double evAtk;
    double evSpaAtk;
    double evDef;
    double evSpaDef;
    double evSpeed;

    public Ev(double evHp, double evAtk, double evSpaAtk, double evDef, double evSpaDef,
            double evSpeed) {
                this.evHp = evHp;
                this.evAtk = evAtk;
                this.evSpaAtk = evSpaAtk;
                this.evDef = evDef;
                this.evSpaDef = evSpaDef;
                this.evSpeed = evSpeed;
            }
}
